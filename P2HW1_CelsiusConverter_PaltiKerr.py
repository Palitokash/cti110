# Get the Fahrenheit Temperature from Celsius Temperature (C).
C = float(input('Enter the celsius temperature: '))

# Calculate the Fahrenheit Temperature (F)
F = ((9/5) * C) + 32

# Display  the Fahrenheit Temperature
print (str(C) + ' is ' + str(F) + ' degrees fahrenheit ')
